// Create web server
// Create a web server that listens on port 3000. When a request is made to the server, respond with the message "Hello, World!".

const http = require('http');

const server = http.createServer((req, res) => {
  res.end('Hello, World!');
});

server.listen(3000, () => {
  console.log('Server is listening on port 3000');
});
